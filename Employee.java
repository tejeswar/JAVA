class Employee 
{
	private int empId;
	private int workedTime;
	private int overTimePayment;
	
	public void setEmpId(int empId)
	{
		this.empId = empId;
	}
	public int getEmpId()
	{
		return empId;
	}
	public void setWorkedTime(int workedTime)
	{
		this.workedTime = workedTime;
	}
	public int getWorkedTime()
	{
		return workedTime;
	}
	public void setOverTimePayment(int overTimePayment)
	{
		this.overTimePayment = overTimePayment;
	}
	public int getOverTimePayment()
	{
		return overTimePayment;
	}
}
